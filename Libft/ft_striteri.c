#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	slen;

	if (!s)
		return ;
	slen = 0;
	while (s[slen])
	{
		(*f)(slen, s + slen);
		slen++;
	}
}
