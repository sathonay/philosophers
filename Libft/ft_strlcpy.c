#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;

	i = 0;
	if (dstsize)
	{
		while (i < dstsize)
		{
			dst[i] = src[i];
			if (src[i] == '\0')
				return (i);
			i++;
		}
		dst[i - 1] = '\0';
	}
	while (src[i])
		i++;
	return (i);
}
